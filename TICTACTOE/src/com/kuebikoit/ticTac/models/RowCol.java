package com.kuebikoit.ticTac.models;

//POJ0
public class RowCol {

    int row;
    int col;

    RowCol() {

    }

    public RowCol(int row, int col) {
        this.row = row;
        this.col = col;
    }

    public int getRow() {
        return row;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public int getCol() {
        return col;
    }

    public void setCol(int col) {
        this.col = col;
    }
}
